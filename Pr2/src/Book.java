public class Book {
    private String title;
    private String autor;
    public void setTitle(String title)
    {
        this.title = title;
    }
    public void setAutor(String autor)
    {
        this.autor = autor;
    }
    public String getTitle()
    {
        return title;
    }
    public String getAutor()
    {
        return autor;
    }
}
