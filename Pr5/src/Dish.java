import java.awt.*;

public class Dish {
    private double depth;
    private String shape;

    public void setDepth(double depth) {
        this.depth = depth;
    }

    public void setShape(String shape) {
        this.shape = shape;
    }

    public double getDepth() {
        return depth;
    }

    public String getShape() {
        return shape;
    }
}

